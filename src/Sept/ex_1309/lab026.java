package Sept.ex_1309;

public class lab026 {
    public static void main(String[] args) {
        // Constant  : whose value can't be changed once assigned
        // use special keyword : final
        // final dataType CONSTANT_NAME = Value;
        final float PIE = 3.1456f;

        // PIE= 6.14F;   --> IT IS NOT POSSIBLE TO CHANGE FINAL VALUE

        System.out.println(PIE);

        int a;
        a = 10;
        System.out.println(a);
    }
}
